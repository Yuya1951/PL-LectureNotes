/**
 * Function to double integers
 *
 * @author Atsushi Igarashi
 * @version 20170117
 *
 */
public class Doubling implements IntToInt {
    // no instance variables

    public Doubling() {
        // nothing to initialize
    }

    /**
     * A method representing a function from integers to integers
     *
     * @param n an integer
     * @return n * 2
     */
    public int apply(int n) { return n * 2; }
}
